function text_value = local_make_measurement_log(measurement)
%LOCAL_MAKE_MEASUREMENT_LOG Сформировать текстовый журнал измерения.

metrics = measurement.metrics;
lines = strings(0, 1);
lines(end + 1, 1) = "Измерение частоты обмена ArduPilot JSON/UDP";
lines(end + 1, 1) = "============================================================";
lines(end + 1, 1) = "Baseline TASK-15 восстановлен      : " + local_bool_text(measurement.baseline_restored);
lines(end + 1, 1) = "Адрес Windows-хоста для WSL2       : " + measurement.host_ip;
lines(end + 1, 1) = "Команда запуска ArduPilot          : " + measurement.launch_command;
lines(end + 1, 1) = "PID arducopter                     : " + string(measurement.launch_pid);
lines(end + 1, 1) = "Процесс жив после запуска          : " + local_bool_text(measurement.process_alive_after_launch);
lines(end + 1, 1) = "Процесс жив после 20 секунд        : " + local_bool_text(measurement.process_alive_after_20s);
lines(end + 1, 1) = "valid_rx_count                     : " + string(metrics.valid_rx_count);
lines(end + 1, 1) = "json_tx_count                      : " + string(metrics.json_tx_count);
lines(end + 1, 1) = "response_tx_count                  : " + string(metrics.response_tx_count);
lines(end + 1, 1) = "invalid_rx_count                   : " + string(metrics.invalid_rx_count);
lines(end + 1, 1) = "udp_rx_datagram_count              : " + string(metrics.udp_rx_datagram_count);
lines(end + 1, 1) = "Средняя частота valid_rx [Hz]      : " + sprintf('%.4f', metrics.valid_rx_rate_hz);
lines(end + 1, 1) = "Средняя частота json_tx [Hz]       : " + sprintf('%.4f', metrics.json_tx_rate_hz);
lines(end + 1, 1) = "Средняя частота response_tx [Hz]   : " + sprintf('%.4f', metrics.response_tx_rate_hz);
lines(end + 1, 1) = "Средний период valid_rx [s]        : " + sprintf('%.6f', metrics.valid_rx_period_mean_s);
lines(end + 1, 1) = "95-процентиль valid_rx [s]         : " + sprintf('%.6f', metrics.valid_rx_period_p95_s);
lines(end + 1, 1) = "Средний период response_tx [s]     : " + sprintf('%.6f', metrics.response_tx_period_mean_s);
lines(end + 1, 1) = "95-процентиль response_tx [s]      : " + sprintf('%.6f', metrics.response_tx_period_p95_s);
lines(end + 1, 1) = "Среднее время шага модели [s]      : " + sprintf('%.8f', metrics.model_step_elapsed_mean_s);
lines(end + 1, 1) = "Среднее время кодирования JSON [s] : " + sprintf('%.8f', metrics.json_encode_elapsed_mean_s);
lines(end + 1, 1) = "Среднее время чтения UDP [s]       : " + sprintf('%.8f', metrics.udp_read_elapsed_mean_s);
lines(end + 1, 1) = "Среднее время записи UDP [s]       : " + sprintf('%.8f', metrics.udp_write_elapsed_mean_s);
lines(end + 1, 1) = "Среднее время цикла MATLAB [s]     : " + sprintf('%.8f', metrics.loop_elapsed_mean_s);
lines(end + 1, 1) = "Прирост frame_count                : " + string(metrics.frame_count_delta);
lines(end + 1, 1) = "Последний frame_count              : " + string(metrics.last_frame_count);
lines(end + 1, 1) = "Последний sender address           : " + string(metrics.last_sender_address);
lines(end + 1, 1) = "Последний sender port              : " + string(metrics.last_sender_port);
lines(end + 1, 1) = "Последний magic                    : " + string(metrics.last_magic);
lines(end + 1, 1) = "Последние ШИМ [us]                 : [" + local_format_vector(metrics.last_pwm_us) + "]";
lines(end + 1, 1) = "Последние motor_cmd [rad/s]        : [" + local_format_vector(metrics.last_motor_cmd_radps) + "]";
lines(end + 1, 1) = "Норма истинного кватерниона конечна: " + local_bool_text(metrics.quat_true_finite);
lines(end + 1, 1) = "Норма оцененного кватерниона конечна: " + local_bool_text(metrics.quat_est_finite);
lines(end + 1, 1) = "Итоговый статус обмена             : " + string(metrics.last_exchange_status);
lines(end + 1, 1) = "Первая причина отказа              : " + string(measurement.first_failure_reason);
text_value = strjoin(lines, newline) + newline;
end

function text_value = local_format_vector(vec_value)
%LOCAL_FORMAT_VECTOR Сформировать компактную строку вектора.

text_value = strtrim(sprintf('%.6f ', double(vec_value(:))));
end

function text_value = local_bool_text(flag_value)
%LOCAL_BOOL_TEXT Преобразовать логический признак в русскую строку.

if flag_value
    text_value = "да";
else
    text_value = "нет";
end
end

function local_write_utf8_text(path_value, text_value)
%LOCAL_WRITE_UTF8_TEXT Сохранить текст UTF-8 без BOM.

folder_path = fileparts(path_value);
if ~isempty(folder_path) && ~isfolder(folder_path)
    mkdir(folder_path);
end

fid = fopen(path_value, 'w', 'n', 'UTF-8');
if fid < 0
    error( ...
        'uav:task17:measure_rate:OpenFile', ...
        'Не удалось открыть файл %s.', ...
        path_value);
end

cleanup_obj = onCleanup(@() fclose(fid)); %#ok<NASGU>
fprintf(fid, '%s', char(text_value));
end
