%% MEASURE_ARDUPILOT_JSON_EXCHANGE_RATE Измерить частоту обмена после восстановления baseline.
% Назначение:
%   Повторно использует восстановленный 20-секундный обмен TASK-15 как
%   единственную допустимую базу для измерения частоты `ArduPilot JSON/UDP`.
%   Сначала запускает `run_ardupilot_task15_baseline_exchange.m`, затем
%   формирует компактный табличный и текстовый отчеты с частотами,
%   периодами и счетчиками обмена.
%
% Входы:
%   none
%
% Выходы:
%   task_17_exchange_rate_measurement - структура результата в базовом
%   рабочем пространстве MATLAB
%
% Единицы измерения:
%   время - секунды;
%   частота - герцы;
%   ШИМ - микросекунды;
%   команды частоты вращения винтов - рад/с
%
% Допущения:
%   Измерение частоты TASK-17 допустимо только при восстановленном
%   baseline-обмене TASK-15.

repo_root = fileparts(fileparts(mfilename('fullpath')));
reports_dir = fullfile(repo_root, 'artifacts', 'reports');
logs_dir = fullfile(repo_root, 'artifacts', 'logs');
baseline_script_path = fullfile(repo_root, 'scripts', 'run_ardupilot_task15_baseline_exchange.m');
summary_log_path = fullfile(logs_dir, 'task_17_exchange_rate_measurement.txt');
csv_report_path = fullfile(reports_dir, 'task_17_exchange_rate_measurement.csv');
mat_report_path = fullfile(reports_dir, 'task_17_exchange_rate_measurement.mat');

if ~isfolder(reports_dir)
    mkdir(reports_dir);
end

if ~isfolder(logs_dir)
    mkdir(logs_dir);
end

run(baseline_script_path);
baseline = evalin('base', 'task_17_task15_baseline_exchange');
metrics = baseline.metrics;

measurement = struct();
measurement.host_ip = string(baseline.cfg.udp_remote_ip);
measurement.launch_command = string(baseline.launch_command);
measurement.launch_pid = double(baseline.process_after_launch.pid);
measurement.process_alive_after_launch = logical(baseline.process_after_launch.is_alive);
measurement.process_alive_after_20s = logical(baseline.process_after_live.is_alive);
measurement.baseline_restored = logical(baseline.baseline_restored);
measurement.first_failure_reason = string(baseline.first_failure_reason);
measurement.metrics = metrics;
measurement.baseline = baseline;

save(mat_report_path, 'measurement');
writetable(local_make_measurement_table(measurement), csv_report_path);
local_write_utf8_text(summary_log_path, local_make_measurement_log(measurement));

assignin('base', 'task_17_exchange_rate_measurement', measurement);

fprintf('Измерение частоты обмена ArduPilot JSON/UDP\n');
fprintf('  baseline восстановлен            : %s\n', local_bool_text(measurement.baseline_restored));
fprintf('  valid_rx_count                   : %d\n', metrics.valid_rx_count);
fprintf('  json_tx_count                    : %d\n', metrics.json_tx_count);
fprintf('  response_tx_count                : %d\n', metrics.response_tx_count);
fprintf('  средняя частота valid_rx [Hz]    : %.4f\n', metrics.valid_rx_rate_hz);
fprintf('  средняя частота response_tx [Hz] : %.4f\n', metrics.response_tx_rate_hz);
fprintf('  p95 периода valid_rx [s]         : %.6f\n', metrics.valid_rx_period_p95_s);
fprintf('  p95 периода response_tx [s]      : %.6f\n', metrics.response_tx_period_p95_s);
fprintf('  last_frame_count                 : %d\n', metrics.last_frame_count);

if ~measurement.baseline_restored
    error( ...
        'uav:task17:measure_rate:BaselineNotRestored', ...
        'Baseline TASK-15 не восстановлен; измерение частоты TASK-17 непригодно. Причина: %s', ...
        measurement.first_failure_reason);
end

